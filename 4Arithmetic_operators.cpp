/************************************
Arithmetic operators in c++
************************************/
#include<iostream>
using namespace std;

int main(){
 
 int var_1 = 30;
 int var_2 = 20;
 //addition operator +
 cout<< var_1 + var_2<<endl;
 //subtraction operator -
 cout<< var_1 - var_2<<endl;
 //multiplication operator *
 cout<< var_1 * var_2<<endl;
 //division operator /
 cout<< var_1 / var_2<<endl;
 //modulo operator +
 cout<< var_1 % var_2<<endl;
 

}
